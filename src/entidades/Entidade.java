package entidades;
// Essa classe vai tornar as coisas mais dificeis
public class Entidade {

        private int id;
        private String nome;
    
        public void setId(int id) {
            this.id = id;
        }
        public void setNome(String nome) {
            this.nome = nome;
        }
        
        public int getId() {
            return id;
        }
        public String getNome() {
            return nome;
        }
       
    
}
